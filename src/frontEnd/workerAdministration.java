    public void refresh() {
        ArrayList wrks = ea.getAllWorkers(); 
        Object[][] data = new Object[wrks.size()][5];
        for (int i = 0; i < wrks.size(); i++) {
            Worker wk = (Worker) wrks.get(i);
            data[i][0] = wk.getNombre();
            data[i][1] = wk.getDpi();
            data[i][2] = wk.getBanco();
            data[i][3] = wk.getCuenta();
            data[i][4] = wk.getSueldo();
        }
        jTable1.setModel(new javax.swing.table.DefaultTableModel(
            data,
            new String [] {
                "Nombre", "DPI", "Banco", "Cuenta", "Sueldo"
            }
        ) {
            boolean[] canEdit = new boolean [] {
                false, false, false, false, false
            };

            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit [columnIndex];
            }
        });
    }
