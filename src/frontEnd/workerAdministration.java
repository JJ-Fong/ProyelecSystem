    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow(); 
        if (row > -1) {
            String nombre = (String) jTable1.getValueAt(row, 0); 
            String dpi = (String) jTable1.getValueAt(row, 1); 
            String banco = (String) jTable1.getValueAt(row, 2); 
            String cuenta = (String) jTable1.getValueAt(row, 3); 
            String sld = String.valueOf(jTable1.getValueAt(row, 4));
            double sueldo = Double.parseDouble(sld); 
            temp = new Worker(row,nombre,dpi,banco,cuenta,sueldo); 
            modworker = true; 
            this.dispose();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow(); 
        if (row > -1) {
            ea.deleteWorker(row+1);
            refresh();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void formWindowClosed(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosed
        // TODO add your handling code here:
        WorkerInfo wi; 
        if (newworker) { 
            wi  = new WorkerInfo(); 
            wi.setVisible(true); 
        } else {
            if (modworker) {
                wi  = new WorkerInfo(temp); 
                wi.setVisible(true);
            } else {
                MenuInicio mI = new MenuInicio(); 
                mI.setVisible(true);
            }
        }
         
    }//GEN-LAST:event_formWindowClosed

